/**
 * Get initials from application name
 */
export const getInitials = (name: string): string => {
  if (!name) return '';
  return name.split(' ')
    .map(part => part.charAt(0))
    .join('')
    .toUpperCase()
    .substring(0, 2);
};

/**
 * Find the best matching logo URL based on application name and fallback attempt
 */
export const getLogoUrl = (name: string, fallbackAttempt: number): string | null => {
  const nameLower = name.toLowerCase();
  const logoMappings = getLogoMappings();

  // Find best match in our mappings
  for (const [key, urls] of Object.entries(logoMappings)) {
    if (nameLower.includes(key) || key.includes(nameLower)) {
      if (fallbackAttempt < urls.length) {
        return urls[fallbackAttempt];
      }
    }
  }

  // Generic fallbacks based on the first word in app name
  const fallbacks = [
    `https://logo.clearbit.com/${nameLower.split(' ')[0]}.com`,
    `https://logo.clearbit.com/${nameLower.replace(/\s+/g, '')}.com`,
    `https://ui-avatars.com/api/?name=${encodeURIComponent(name)}&background=f1f5f9&color=475569&bold=true`,
    `https://source.boringavatars.com/beam/120/${encodeURIComponent(name)}?colors=264653,2a9d8f,e9c46a,f4a261,e76f51`
  ];
  
  return fallbackAttempt < fallbacks.length ? fallbacks[fallbackAttempt] : null;
};

